class TestPerCallFormatOverride:
    """agent_format can be overridden per score_chain() call."""

    def test_openai_override_on_react_guard(self):
        from llm_guard.agent_guard import AgentGuard
        guard = AgentGuard()  # default react
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            r = guard.score_chain(
                "Who wrote Hamlet?",
                OPENAI_CLEAN,
                "William Shakespeare",
                agent_format="openai"
            )
        assert 0.0 <= r.risk_score <= 1.0

    def test_autogen_override(self):
        from llm_guard.agent_guard import AgentGuard
        guard = AgentGuard()
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            r = guard.score_chain(
                "Who wrote Hamlet?",
                AUTOGEN_CLEAN,
                "William Shakespeare",
                agent_format="autogen"
            )
        assert 0.0 <= r.risk_score <= 1.0

    def test_auto_detect_works_for_all_formats(self):
        """auto format detection should work for each format."""
        from llm_guard.agent_guard import AgentGuard
        guard = AgentGuard(agent_format="auto")
        formats_and_chains = [
            (OPENAI_CLEAN,    "William Shakespeare"),
            (LANGGRAPH_CLEAN, "Paris"),
            (AUTOGEN_CLEAN,   "William Shakespeare"),
        ]
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            for steps, ans in formats_and_chains:
                r = guard.score_chain("Q?", steps, ans)
                assert 0.0 <= r.risk_score <= 1.0
                assert r.confidence_tier in ("HIGH", "MEDIUM", "LOW")


