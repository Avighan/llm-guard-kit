class TestOpenAIScoring:
    """Scoring valid after OpenAI-format normalisation."""

    def test_risk_in_range(self):
        from llm_guard.agent_guard import AgentGuard
        guard = AgentGuard(agent_format="openai")
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            r = guard.score_chain("Who wrote Hamlet?", OPENAI_CLEAN, "William Shakespeare")
        assert 0.0 <= r.risk_score <= 1.0

    def test_tier_valid(self):
        from llm_guard.agent_guard import AgentGuard
        guard = AgentGuard(agent_format="openai")
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            r = guard.score_chain("Who wrote Hamlet?", OPENAI_CLEAN, "William Shakespeare")
        assert r.confidence_tier in ("HIGH", "MEDIUM", "LOW")

    def test_looping_scores_higher_than_clean(self):
        from llm_guard.agent_guard import AgentGuard
        guard = AgentGuard(agent_format="openai")
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            r_clean = guard.score_chain("Who wrote Hamlet?", OPENAI_CLEAN, "William Shakespeare")
            r_loop  = guard.score_chain("Who wrote X?", OPENAI_LOOP, "unknown")
        assert r_loop.risk_score > r_clean.risk_score, (
            f"CLAIM: Looping chain ({r_loop.risk_score:.3f}) should score higher than "
            f"clean chain ({r_clean.risk_score:.3f}) for OpenAI-tools format.\n"
            f"NOTE: AUROC numbers not validated for OpenAI-tools format. "
            f"Structural signal only validated here."
        )

    def test_format_warning_emitted(self):
        from llm_guard.agent_guard import AgentGuard
        guard = AgentGuard(agent_format="openai")
        with warnings.catch_warnings(record=True) as w:
            warnings.simplefilter("always")
            guard.score_chain("Who wrote Hamlet?", OPENAI_CLEAN, "William Shakespeare")
            format_warns = [x for x in w if "normalised" in str(x.message).lower()
                           or "openai" in str(x.message).lower()
                           or "format" in str(x.message).lower()]
            assert len(format_warns) >= 1, "Expected format warning for non-native OpenAI format"


