class TestFrameworkCoverageDisclaimer:
    """
    Documents which framework claims are validated vs. plausible-but-not-measured.

    STRUCTURAL claims (validated here):
      - Normalisation produces canonical {thought, action_type, action_arg, observation}
      - risk_score in [0, 1] for all frameworks
      - confidence_tier in HIGH/MEDIUM/LOW for all frameworks
      - Looping chains score higher risk than clean chains (directional validity)
      - UserWarning emitted for non-native formats

    AUROC claims (NOT validated for non-ReAct frameworks):
      - AUROC 0.812 within-domain: ONLY validated on Claude-generated ReAct chains, HotpotQA
      - AUROC 0.741 cross-domain: ONLY validated on Claude-generated ReAct chains, NQ
      - For OpenAI/LangGraph/AutoGen/LangChain: structural signals are model-agnostic,
        but AUROC on your specific framework+domain needs independent measurement.

    To validate for your framework:
      1. Collect 200+ chain runs with ground-truth correctness labels
      2. from llm_guard.local_verifier import LocalVerifier, extract_features
      3. Run 5-fold CV AUROC (sklearn.model_selection.cross_val_score)
      4. Compare to the 0.812 baseline
    """

    def test_coverage_matrix_is_documented(self):
        """Structural: all frameworks produce valid output. AUROC claims need per-framework validation."""
        coverage = {
            "react":     {"structural": True, "auroc_validated": True,  "auroc_value": 0.812},
            "openai":    {"structural": True, "auroc_validated": False, "auroc_value": None},
            "langgraph": {"structural": True, "auroc_validated": False, "auroc_value": None},
            "autogen":   {"structural": True, "auroc_validated": False, "auroc_value": None},
            "langchain": {"structural": True, "auroc_validated": False, "auroc_value": None},
        }
        # All frameworks validated structurally
        for fmt, info in coverage.items():
            assert info["structural"], f"{fmt} structural validation missing"

        # Only ReAct has held-out AUROC
        validated = [fmt for fmt, info in coverage.items() if info["auroc_validated"]]
        assert validated == ["react"], (
            "Only ReAct format has held-out AUROC validation. "
            "Other frameworks: structural output validated, AUROC needs per-deployment measurement."
        )
